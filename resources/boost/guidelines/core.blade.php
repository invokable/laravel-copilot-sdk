## Laravel Copilot SDK

- This application uses the Laravel Copilot SDK (`revolution/laravel-copilot-sdk`) to interact with GitHub Copilot CLI programmatically via JSON-RPC.
- Use `Copilot::run()` for single prompts, `Copilot::start()` for multi-turn sessions, and `Copilot::fake()` for testing.
